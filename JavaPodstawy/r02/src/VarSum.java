public class VarSum {
    public static void main(String[] args) {

        int i;
        int sum = 0;

        for (i = 0; i < 10; i++){

            sum += i;
            System.out.println("Suma: " + sum);
        }
        System.out.println();
        System.out.println("Suma: " + sum);

    }
}
