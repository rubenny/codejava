// Zmienne typu char można traktować jak wartości całkowite.
class CharArithDemo {
    public static void main(String args[]) {
        char ch;

        ch = 'X';
        System.out.println("ch zawiera " + ch);

        ch++; // inkrementacja ch
        System.out.println("a teraz " + ch);

        ch = 90; // przypisuje ch litere Z
        System.out.println("a teraz " + ch);
    }
}
